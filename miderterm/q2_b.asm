List p=18f4520
    #include<p18f4520.inc>
    CONFIG OSC=INTIO67
    CONFIG WDT=OFF
    org 0x00
first:
    MOVLW 0X03
    MOVWF 0X10
    MOVLW 0X43
    MOVWF 0X11
    MOVLW 0X78
    MOVWF 0X12
second:
    MOVLW 0X22
    MOVWF 0X20
    MOVLW 0X23
    MOVWF 0X21
    MOVLW 0X24
    MOVWF 0X22
third:
    MOVLW 0X56
    MOVFF WREG,0X100
    MOVLW 0X57
    MOVFF WREG,0X101
    MOVLW 0X96
    MOVFF WREG,0X102
merge:
    MOVFF 0X010,0X000
    MOVFF 0X011,0X001
    MOVFF 0X012,0X002
    MOVFF 0X020,0X003
    MOVFF 0X021,0X004
    MOVFF 0X022,0X005
    MOVFF 0X100,0X006
    MOVFF 0X101,0X007
    MOVFF 0X102,0X008
pre_set:
    CLRF 0X10
    CLRF 0X11
    CLRF 0X12
sort:
    LFSR 0,0X000 ;LEFT
    LFSR 2,0X008 ;RIGHT
    LFSR 1,0X000 ;NOW
    MOVLW 0X08
    MOVWF 0X10 ;OUT_COUNTER
    CLRF 0X11 ;IN_COUNTER
    CLRF 0X12 ;REG
loop:
    MOVFF 0X10,0X11
big:
    MOVFF POSTINC1,WREG
    CPFSGT INDF1
	CALL change_big
    DECFSZ 0x11,F
	BRA big
    MOVFF 0X10,0X11
small:
    MOVFF POSTDEC1,WREG
    CPFSLT INDF1
	CALL change_small
    DECFSZ 0x11,F
	BRA small
    DECFSZ 0X10,F
	BRA loop
    BRA finish
change_big:
    MOVFF POSTDEC1,0X12
    MOVFF 0X12,POSTINC1
    MOVFF WREG,INDF1
    RETURN
change_small:
    MOVFF POSTINC1,0X12
    MOVFF 0X12,POSTDEC1
    MOVFF WREG,INDF1
    RETURN
finish:    
    NOP
    end
    
    


