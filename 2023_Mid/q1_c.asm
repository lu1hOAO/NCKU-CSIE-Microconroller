List p=18f4520
    #include<p18f4520.inc>
    CONFIG OSC=INTIO67
    CONFIG WDT=OFF
    org 0x00
    MOVLW 0X01
    MOVFF WREG,0X11
    MOVFF WREG,0X13
    MOVLW 0X11
    MOVWF 0X30
    CLRF WREG
    INCF WREG
    INCF WREG
    CPFSGT 0X30
	BRA finish
    SUBWF 0X30,F
	
    
loop:
    MOVFF 0X11,0X21
    MOVFF 0X10,0X20
    MOVFF 0X13,WREG
    ADDWF 0X11,F
    MOVFF 0X12,WREG
    ADDWFC 0X10,F
    MOVFF 0X21,0X13
    MOVFF 0X20,0X12
    DECFSZ 0X30,F
	BRA loop
    BRA finish
finish:
    MOVFF 0X11,0X01
    MOVFF 0X10,0X00
    MOVFF 0X13,0X11
    MOVFF 0X12,0X10
    CLRF 0X12
    CLRF 0X13
    NOP
    end
  
    
    
    


