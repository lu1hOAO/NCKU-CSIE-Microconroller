List p=18f4520
    #include<p18f4520.inc>
    CONFIG OSC=INTIO67
    CONFIG WDT=OFF
    org 0x00
    LFSR 0,0X002
    LFSR 1,0X000
    LFSR 2,0X001
    MOVLW 0X09
    MOVWF 0X11
    CLRF WREG
    INCF WREG
    CPFSGT 0X11
	BRA fst
    INCF WREG
    CPFSGT 0X11
	BRA sst
    SUBWF 0X11,F
    MOVLW 0X01
    MOVFF WREG,0X00
    MOVFF WREG,0X01
    
fib:
    MOVFF POSTINC1,WREG
    ADDWF POSTINC2,W
    MOVFF WREG,POSTINC0
    DECFSZ 0X11
	BRA fib
    BRA finish
fst:
    MOVFF 0X11,0X00
    BRA finish
sst:
    MOVFF 0x11,0x00
    MOVFF 0x11,0x01
    BRA finish
finish:
    NOP
    end

