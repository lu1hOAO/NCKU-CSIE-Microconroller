List p=18f4520
    #include<p18f4520.inc>
    CONFIG OSC=INTIO67
    CONFIG WDT=OFF
    org 0x00
    MOVLW 0X05
    MOVWF 0X10
    CLRF WREG
    CPFSGT 0X10
	BRA a0
    INCF WREG
    CPFSGT 0X10
	BRA a1
    INCF WREG
    CPFSGT 0X10
	BRA a2
    INCF WREG
    CPFSGT 0X10
	BRA a3
    SUBWF 0X10,F
    MOVLW 0X01
    MOVWF 0X20
    MOVLW 0X02
    MOVWF 0X21
    MOVLW 0X04
    MOVWF 0X22
    LFSR 0,0X020
    LFSR 1,0X021
    LFSR 2,0X022
loop:
    MOVFF POSTINC0,WREG
    ADDWF POSTINC1,W
    ADDWFC POSTINC2,W
    MOVFF WREG,INDF2
    DECFSZ 0X10
	BRA loop
    BRA next
a0:
    CLRF 0X00
    BRA finish
a1:
    MOVLW 0X01
    MOVFF WREG,0X00
    BRA finish
a2:
    MOVLW 0X02
    MOVFF WREG,0X00
    BRA finish
a3:
    MOVLW 0X04
    MOVFF WREG,0X00
    BRA finish
next:
    MOVFF INDF2,0X00
finish:
    NOP
    end


