List p=18f4520
    #include<p18f4520.inc>
    CONFIG OSC=INTIO67
    CONFIG WDT=OFF
    org 0x00
    MOVLW 0X46
    MOVWF 0X00
    MOVLW 0X5A
    MOVWF 0X01
    MOVLW 0X04
    MOVWF 0X02
    MOVLW 0X12
    MOVWF 0X03
    MOVLW 0X2A
    MOVWF 0X04
    MOVLW 0XDD
    MOVWF 0X05
    MOVLW 0X21
    MOVWF 0X06
    MOVLW 0XBF
    MOVWF 0X07
    MOVLW 0X08
    MOVWF 0X10 ;COUNTER
    LFSR 0,0X07;PIVOT
    LFSR 1,0X00; LEFT
    LFSR 2,0X00;INCF
loop:
    MOVFF INDF0,WREG
    CPFSGT POSTINC2
	CALL change
    DECFSZ 0X10
	BRA loop
    BRA finish
change:
    CLRF WREG
    ADDWF POSTDEC2,F
    MOVFF INDF2,0X11
    MOVFF INDF1,POSTINC2
    MOVFF 0X11,POSTINC1
    RETURN
finish:
    NOP
    end
    
    

